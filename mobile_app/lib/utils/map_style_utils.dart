  /// Returns the base map style for the current theme.
  static String styleForBrightness(Brightness brightness) {
    return brightness == Brightness.dark ? defaultDarkStyle : defaultLightStyle;
  }

  /// Returns a map style that highlights tennis courts for the current theme.
  static String tennisCourtStyleForBrightness(Brightness brightness) {
    final List<dynamic> baseStyleJson =
        json.decode(styleForBrightness(brightness)) as List<dynamic>;
    final List<dynamic> tennisStyleJson =
        json.decode(tennisCourtStyle) as List<dynamic>;

    return json.encode(<dynamic>[
      ...baseStyleJson,
      ...tennisStyleJson,
    ]);
  }

  /// Checks if a map style is valid.
  ///
  /// [style] is the map style JSON string.
  ///
  /// Returns `true` if the style is valid, `false` otherwise.
  static bool isValidMapStyle(String style) {
    try {
      json.decode(style);
      return true;
    } catch (e) {
      return false;
    }
  }
